package com.jimo.model;

public class Firm {
	private String firmName;
	private String firmDesc;
	private String firmTel;

	public String getFirmName() {
		return firmName;
	}

	public void setFirmName(String firmName) {
		this.firmName = firmName;
	}

	public String getFirmDesc() {
		return firmDesc;
	}

	public void setFirmDesc(String firmDesc) {
		this.firmDesc = firmDesc;
	}

	public String getFirmTel() {
		return firmTel;
	}

	public void setFirmTel(String firmTel) {
		this.firmTel = firmTel;
	}
}
